using UnityEngine;
using EnumType; // 1단계에서 GlobalEnum에 작성해 둔 KimEnemyState 열거형을 사용하기 위해 불러옵니다.

/// <summary>
/// 적 캐릭터의 행동 규격 약속서 인터페이스입니다.
/// 작동 원리 설명:
///  - 이 파일은 인터페이스라고 부릅니다.
///  - 앞으로 우리가 4단계에서 빚어낼 적의 모든 상태 행동들(대기, 순찰, 추격, 공격, 죽음 등)이 반드시 한 조각의 빈틈도 없이 지켜야 할 법적 약속(규격)을 정해두는 헌법 책입니다.
///  - 인터페이스는 내부에 실질적인 달리기나 공격 물리 기능을 직접 코딩하지 않고, 오직 "이러이러한 형태의 이름과 모양을 갖춘 세 가지 함수를 무조건 만들어라"라고 가이드라인만 정의합니다.
///  - 이렇게 규칙을 세워두면, 3단계로 조립할 본체 컨트롤러가 개별 상태들의 내부 세부 코드를 일일이 몰라도 이 인터페이스 약속만 보고 편안하고 안전하게 상태를 교체하며 조종할 수 있게 됩니다.
/// </summary>
public interface IKimEnemyState
{
    // [상태 입장 규칙]
    // 적이 이 상태에 처음 발을 들여놓는 찰나에 딱 한 번 실행되는 함수입니다.
    // 예를 들어 순찰 상태에 들어오는 첫 프레임에 순찰 걷기 애니메이션을 틀거나, 대기 타이머를 3초로 충전할 때 사용합니다.
    // 매개변수로 적 본체(KimEnemy)를 통째로 전달받아 그 본체의 컴포넌트(물리 리지드바디, 애니메이터 등)를 쥘 수 있게 해 줍니다.
    void EnterState(KimEnemy enemy);

    // [상태 유지 규칙]
    // 적이 이 상태를 계속 유지하는 동안 실시간(매 프레임)으로 무한히 반복해서 실행되는 함수입니다.
    // 유니티의 Update 함수처럼 실시간으로 플레이어가 사정거리 내에 들어왔는지 감시하거나 지면을 딛고 달리게 만드는 물리 연산을 쉬지 않고 처리합니다.
    void UpdateState(KimEnemy enemy);

    // [상태 퇴장 규칙]
    // 다른 다음 상태(예: 순찰에서 추격으로)로 넘어가기 직전, 현재 상태를 빠져나오는 찰나에 딱 한 번 실행되는 함수입니다.
    // 예를 들어 상태를 끝내면서 켜져 있던 쉴드 타이머를 안전하게 끄거나, 미처 치우지 못한 잔여 데이터들을 청소할 때 사용합니다.
    void ExitState(KimEnemy enemy);
}